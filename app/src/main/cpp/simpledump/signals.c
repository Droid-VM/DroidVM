const char* x_sig_name(int sig) {
    const char *ret = NULL;
    if (sig >= 0 && sig < (int)ARRAY_LEN(str_signal_name))
        ret = str_signal_name[sig];
    if (!ret) ret = "Unknown";
    return ret;
}

const char* x_sig_desc(int sig) {
    const char *ret = NULL;
    if (sig >= 0 && sig < (int)ARRAY_LEN(str_signal_desc))
        ret = str_signal_desc[sig];
    if (!ret) ret = "unknown";
    return ret;
}
